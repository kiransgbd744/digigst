package com.ey.advisory.app.data.services.ewb;

public class VehicleDetails {

	
	private String mode;
	private String transporterDocNo;
	private String enteredDate;
	private String from;
	private String enteredBy;
	private String cewbNo;
	private String mulVehNo;
	
	public String getMode() {
		return mode;
	}
	public void setMode(String mode) {
		this.mode = mode;
	}
	public String getTransporterDocNo() {
		return transporterDocNo;
	}
	public void setTransporterDocNo(String transporterDocNo) {
		this.transporterDocNo = transporterDocNo;
	}
	public String getEnteredDate() {
		return enteredDate;
	}
	public void setEnteredDate(String enteredDate) {
		this.enteredDate = enteredDate;
	}
	public String getFrom() {
		return from;
	}
	public void setFrom(String from) {
		this.from = from;
	}
	public String getEnteredBy() {
		return enteredBy;
	}
	public void setEnteredBy(String enteredBy) {
		this.enteredBy = enteredBy;
	}
	public String getCewbNo() {
		return cewbNo;
	}
	public void setCewbNo(String cewbNo) {
		this.cewbNo = cewbNo;
	}
	public String getMulVehNo() {
		return mulVehNo;
	}
	public void setMulVehNo(String mulVehNo) {
		this.mulVehNo = mulVehNo;
	}
	
}

